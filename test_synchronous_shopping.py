class TestRouteFinder(unittest.TestCase):
    def test_sample_test_case_0(self):
        cache = route_finder(
            vertices=(1, 2, 3, 4, 5),
            edges=(
                Road(route={1, 2}, cost=10),
                Road(route={1, 3}, cost=10),
                Road(route={2, 4}, cost=10),
                Road(route={3, 5}, cost=10),
                Road(route={4, 5}, cost=10),
            ),
        )

        self.assertEqual(
            cache[(1, 5)],
            20,
            'start to end')
        # forward chain
        self.assertIn(
            (1, 2),
            cache)
        self.assertIn(
            (2, 1),
            cache)
        self.assertIn(
            (1, 3),
            cache)
        self.assertIn(
            (3, 1),
            cache)
        self.assertIn(
            (1, 4),
            cache)
        self.assertIn(
            (4, 1),
            cache)
        self.assertIn(
            (1, 5),
            cache)
        self.assertIn(
            (5, 1),
            cache)
        # return chain
        self.assertIn(
            (5, 3),
            cache)
        self.assertIn(
            (3, 5),
            cache)

        self.assertEqual(
            cache[(5, 1)],
            20,
            'end to start')

        self.assertEqual(
            find_route_costs(cache=cache, route_cache={}, route=tuple()),
            0,
            'empty route')

        self.assertEqual(
            find_route_costs(cache=cache, route_cache={}, route=(1, 5, 1)),
            40,
            'start to end and back to start')

        self.assertEqual(
            find_route_costs(cache=cache, route_cache={}, route=(1, 4, 5)),
            30,
            'start to end but the long way')


