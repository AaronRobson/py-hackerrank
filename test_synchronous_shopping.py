class TestAllSplitsInTwo(unittest.TestCase):
    def test_sample_test_case_0(self):
        self.assertEqual(
            list(all_splits_in_two(centers=(2, 3, 4))),
            [
                ((2,), (3, 4)),
                ((2, 3), (4,)),
            ])

    def test_sample_test_case_0_reversed(self):
        self.assertEqual(
            list(all_splits_in_two(centers=(4, 3, 2))),
            [
                ((4,), (3, 2)),
                ((4, 3), (2,)),
            ])

    def test_empty(self):
        self.assertEqual(
            list(all_splits_in_two(centers=tuple())),
            [])

    def test_one(self):
        self.assertEqual(
            list(all_splits_in_two(centers=(42,))),
            [
                (tuple(), (42,)),
            ])

    def test_two(self):
        self.assertEqual(
            list(all_splits_in_two(centers=(42, 43))),
            [
                ((42,), (43,)),
            ])

    def test_three(self):
        self.assertEqual(
            list(all_splits_in_two(centers=(42, 43, 44))),
            [
                ((42,), (43, 44,)),
                ((42, 43), (44,)),
            ])

    def test_odd_palendromic(self):
        self.assertEqual(
            list(all_splits_in_two(centers=(1, 2, 1))),
            [
                ((1,), (2, 1)),
                ((1, 2), (1,)),
            ])

    def test_even_palendromic(self):
        self.assertEqual(
            list(all_splits_in_two(centers=(1, 2, 2, 1))),
            [
                ((1,), (2, 2, 1)),
                ((1, 2), (2, 1,)),
                ((1, 2, 2), (1,)),
            ])

    def test_even_pairs_repeated(self):
        self.assertEqual(
            list(all_splits_in_two(centers=(1, 2, 1, 2))),
            [
                ((1,), (2, 1, 2)),
                ((1, 2), (1, 2,)),
                ((1, 2, 1), (2,)),
            ])


